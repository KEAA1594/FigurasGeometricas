    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String altura = rectangulo.this.txtFieldAltura.getText();
        String base = rectangulo.this.txtFieldBase.getText();
        if(!altura.equals("") && !base.equals("")){
            Crectangulo rectangulo = new Crectangulo();
            double [] results = rectangulo.operaciones(Double.parseDouble(altura),Double.parseDouble(base));
            rectangulo.this.txtFieldArea.setText(String.format("%.4f", results[0]));
            rectangulo.this.txtFieldPerimetro.setText(String.format("%.4f", results[1]));
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

